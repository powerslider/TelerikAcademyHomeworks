using System;

class DeclareFloatingPointVariables
{
    static void Main()
    {
        double a = 34.567839023;
        float b = 12.345f;
        float c = 8923.1234857f;
        decimal d = 3456.091124875956542151256683467m;

        Console.WriteLine(a);
        Console.WriteLine(b);
        Console.WriteLine(c);
        Console.WriteLine(d);
    }
}
