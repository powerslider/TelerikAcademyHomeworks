using System;

class OddOrEvenIntegers
{
    static void Main()
    {
        int x = 3;
        int y = 2;
        Console.WriteLine(x % 2 == 0 ? "X is an even number" : "X is an odd number");
        Console.WriteLine(y % 2 == 0 ? "Y is an even number" : "Y is an odd number");
    }
}

